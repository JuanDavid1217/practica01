    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        long id=0;
        try{
        id=Long.parseLong(entradaID.getText());
        String nombre=entradaNombre.getText();
        String direccion=entradaDireccion.getText();
        
        if(!nombre.equals("")&&!direccion.equals("")&&id!=0){
            Empleado emp=new Empleado();
            emp.setClave(id);
            emp.setNombre(nombre);
            emp.setDireccion(direccion);
            DAOEmpleado daoEmpleado=new DAOEmpleado();
            daoEmpleado.create(emp);
            JOptionPane.showMessageDialog(null, "Empleado registrado correctamente.");
            llenarTabla();
            entradaNombre.setText("");
            entradaDireccion.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Campos vacios.");
        }
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "El campo ID debe ser numerico.");
        }
    }//GEN-LAST:event_saveButtonActionPerformed


